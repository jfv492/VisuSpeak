# Generate a random image
image_width = 640
image_height = 480
image = generate_random_image(image_width, image_height)

# Generate random landmark points for testing
landmark_point = np.random.randint(0, image_width, (21, 2))

# Performance Evaluation
start_time = time.time()

# Call function here
draw_landmarks(image, landmark_point)

end_time = time.time()

execution_time = end_time - start_time
print("Execution Time:", execution_time)

cpu_usage_before = psutil.cpu_percent()
memory_usage_before = psutil.virtual_memory().used

# Call function here
draw_landmarks(image, landmark_point)

cpu_usage_after = psutil.cpu_percent()
